def test_translate_result_returns_agent_response_shape(tmp_path):
    """Translate a fabricated RunResult-like object through _translate_result."""
    runtime = OpenAIAgentsRuntime(tools=_registry(), config=_config(tmp_path))

    class _FakeResult:
        new_items: List[Any] = []
        raw_responses: List[Any] = []
        interruptions: List[Any] = []
        final_output = "all done"

    resp = runtime._translate_result(prompt="hi", result=_FakeResult())
    assert resp.content == "all done"
    assert resp.stopped_reason == "complete"
    assert resp.tool_calls_made == 0
    assert resp.messages[0].role == "user"
    assert resp.messages[0].content == "hi"


def test_translate_result_flags_needs_approval_when_interruptions_present(tmp_path):
    runtime = OpenAIAgentsRuntime(tools=_registry(), config=_config(tmp_path))

    class _FakeResult:
        new_items: List[Any] = []
        raw_responses: List[Any] = []
        interruptions = ["pretend approval item"]
        final_output = ""

    fake = _FakeResult()
    resp = runtime._translate_result(prompt="run a thing", result=fake)
    assert resp.stopped_reason == "needs_approval"
    # The RunState (here our fake) is stashed for the TUI to resume.
    assert runtime.pending_state is fake


