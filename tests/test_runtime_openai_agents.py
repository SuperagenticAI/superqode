"""Smoke tests for the OpenAI Agents runtime adapter."""

from __future__ import annotations

from pathlib import Path
from typing import Any, Dict, List

import pytest

from superqode.agent.loop import AgentConfig
from superqode.runtime import create_runtime
from superqode.runtime.errors import RuntimeNotInstalledError
from superqode.tools.base import Tool, ToolContext, ToolRegistry, ToolResult

pytest.importorskip("agents", reason="openai-agents not installed")

from superqode.runtime.openai_agents import OpenAIAgentsRuntime  # noqa: E402


class _EchoTool(Tool):
    @property
    def name(self) -> str:
        return "echo"

    @property
    def description(self) -> str:
        return "Echo back the input text."

    @property
    def parameters(self) -> Dict[str, Any]:
        return {
            "type": "object",
            "properties": {"text": {"type": "string"}},
            "required": ["text"],
        }

    async def execute(self, args: Dict[str, Any], ctx: ToolContext) -> ToolResult:
        return ToolResult(success=True, output=args.get("text", ""))


def _registry() -> ToolRegistry:
    r = ToolRegistry()
    r.register(_EchoTool())
    return r


def _config(tmp_path: Path) -> AgentConfig:
    return AgentConfig(
        provider="openai",
        model="gpt-4o-mini",
        working_directory=tmp_path,
        enable_session_storage=False,
        session_id="test-oai",
    )


def test_factory_returns_openai_runtime(tmp_path):
    runtime = create_runtime(
        "openai-agents",
        gateway=None,
        tools=_registry(),
        config=_config(tmp_path),
    )
    assert isinstance(runtime, OpenAIAgentsRuntime)
    assert runtime.name == "openai-agents"


def test_runtime_constructs_with_required_fields(tmp_path):
    runtime = OpenAIAgentsRuntime(tools=_registry(), config=_config(tmp_path))
    assert runtime.session_id == "test-oai"
    assert runtime._agent is not None
    assert runtime._run_config.tracing_disabled is True
    # SuperQode echo tool was bridged into the Agent's tool list.
    assert any(getattr(t, "name", "") == "echo" for t in runtime._agent.tools)


def test_runtime_rejects_missing_required(tmp_path):
    with pytest.raises(ValueError):
        OpenAIAgentsRuntime(tools=_registry(), config=None)
    with pytest.raises(ValueError):
        OpenAIAgentsRuntime(tools=None, config=_config(tmp_path))


def test_runtime_ignores_unrelated_kwargs(tmp_path):
    """Constructor parity with BuiltinRuntime — extra kwargs must be tolerated."""
    runtime = OpenAIAgentsRuntime(
        gateway=object(),
        tools=_registry(),
        config=_config(tmp_path),
        mcp_executor=None,
        mcp_tools=[],
        include_mcp=False,
        parallel_tools=True,
        random_unknown="ok",  # collected by **_unused
    )
    assert runtime.name == "openai-agents"


