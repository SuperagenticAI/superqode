def test_cancel_sets_flag(tmp_path):
    runtime = OpenAIAgentsRuntime(tools=_registry(), config=_config(tmp_path))
    assert runtime._cancelled is False
    runtime.cancel()
    assert runtime._cancelled is True
    runtime.reset_cancellation()
    assert runtime._cancelled is False


